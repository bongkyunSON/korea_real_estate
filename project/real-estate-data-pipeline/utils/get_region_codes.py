def get_seoul_sigungu_codes():
    """
    사전에 정의된 서울특별시 시군구 목록을 기반으로 법정동 코드를 반환합니다.
    API 호출에 필요한 5자리 코드 형식으로 가공합니다.
    
    Returns:
        dict: {'종로구': '11110', '중구': '11140', ...} 형태의 딕셔너리
    """

    seoul_gungu_list = [
        ("1111000000", "종로구"),
        ("1114000000", "중구"), 
        ("1117000000", "용산구"),
        ("1120000000", "성동구"),
        ("1121500000", "광진구"),
        ("1123000000", "동대문구"),
        ("1126000000", "중랑구"),
        ("1129000000", "성북구"),
        ("1130500000", "강북구"),
        ("1132000000", "도봉구"),
        ("1135000000", "노원구"),
        ("1138000000", "은평구"),
        ("1141000000", "서대문구"),
        ("1144000000", "마포구"),
        ("1147000000", "양천구"),
        ("1150000000", "강서구"),
        ("1153000000", "구로구"),
        ("1154500000", "금천구"),
        ("1156000000", "영등포구"),
        ("1159000000", "동작구"),
        ("1162000000", "관악구"),
        ("1165000000", "서초구"),
        ("1168000000", "강남구"),
        ("1171000000", "송파구"),
        ("1174000000", "강동구")
    ]
    
    sigungu_codes = {}
    for code, district in seoul_gungu_list:
        # 10자리 코드의 앞 5자리만 잘라서 사용
        api_code = code[:5]
        sigungu_codes[district] = api_code
        
    return sigungu_codes

if __name__ == "__main__":
    seoul_codes = get_seoul_sigungu_codes()
    
    print("--- 서울특별시 시군구 코드 목록 (사전 정의) ---")
    for district, code in seoul_codes.items():
        print(f"{district}: {code}")
        
    print(f"\n총 {len(seoul_codes)}개의 자치구를 찾았습니다.")